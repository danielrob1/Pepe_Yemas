def main():
    # Crear carpeta de destino para labels si no existe
    if not os.path.exists(dst_dir):
        os.makedirs(dst_dir)
        print(f"Directorio de destino creado: {dst_dir}")

    all_files = os.listdir(src_dir)
    count = 0
    not_found = []

    print("Iniciando copia de archivos .txt...")

    for target in targets:
        t = target.strip()
        
        if '_' in t:
            parte_a, parte_b = t.split('_')
            
            # El patrón busca: ParteA + CualquierCosa + _ + ParteB + .txt
            # El $ asegura que termine exactamente en .txt
            patron = re.compile(rf".*{parte_a}.*_{parte_b}\.txt$", re.IGNORECASE)
            
            found = False
            for filename in all_files:
                if patron.match(filename):
                    src_path = os.path.join(src_dir, filename)
                    dst_path = os.path.join(dst_dir, filename)
                    
                    shutil.copy2(src_path, dst_path)
                    print(f"[OK] Label copiado: {filename}")
                    found = True
                    count += 1
                    break
            
            if not found:
                not_found.append(t)

    print("\n" + "="*40)
    print(f"RESUMEN DE LABELS:")
    print(f"Archivos .txt copiados: {count}")
    if not_found:
        print(f"No encontrados ({len(not_found)}): {', '.join(not_found)}")
    print("="*40)

if __name__ == "__main__":
    main()